public class Skorlar {
    private  String dogruSayisi;
    private  String yanlisSayisi;
    private  String sonuc;
    private  String tarih;

    public String getDogruSayisi() {
        return dogruSayisi;
    }

    public void setDogruSayisi(String dogruSayisi) {
        this.dogruSayisi = dogruSayisi;
    }

    public String getYanlisSayisi() {
        return yanlisSayisi;
    }

    public void setYanlisSayisi(String yanlisSayisi) {
        this.yanlisSayisi = yanlisSayisi;
    }

    public String getSonuc() {
        return sonuc;
    }

    public void setSonuc(String sonuc) {
        this.sonuc = sonuc;
    }

    public String getTarih() {
        return tarih;
    }

    public void setTarih(String tarih) {
        this.tarih = tarih;
    }
}
